package com.old.silence.content.domain.model;


import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.OneToMany;

import java.math.BigInteger;
import java.util.List;

import com.old.silence.content.domain.enums.ContentTagType;
import com.old.silence.data.commons.domain.AbstractAutoGeneratedIdAuditable;

@Entity
public class ContentTag extends AbstractAutoGeneratedIdAuditable<BigInteger> {

    private String name;

    @Column(updatable = false)
    private String code;

    @Column(updatable = false)
    private ContentTagType type;

    @Column(name = "is_enabled")
    private Boolean enabled;

    private BigInteger parentId;

    private String iconReference;
    private String description;

    private Long displayOrder;

    @OneToMany(mappedBy = "contentTag")
    private List<ContentContentTag> contentContentTags;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public ContentTagType getType() {
        return type;
    }

    public void setType(ContentTagType type) {
        this.type = type;
    }

    public BigInteger getParentId() {
        return parentId;
    }

    public void setParentId(BigInteger parentId) {
        this.parentId = parentId;
    }

    public Long getDisplayOrder() {
        return displayOrder;
    }

    public void setDisplayOrder(Long displayOrder) {
        this.displayOrder = displayOrder;
    }

    public Boolean getEnabled() {
        return enabled;
    }

    public void setEnabled(Boolean enabled) {
        this.enabled = enabled;
    }

    public String getIconReference() {
        return iconReference;
    }

    public void setIconReference(String iconReference) {
        this.iconReference = iconReference;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<ContentContentTag> getContentContentTags() {
        return contentContentTags;
    }

    public void setContentContentTags(List<ContentContentTag> contentContentTags) {
        this.contentContentTags = contentContentTags;
    }
}