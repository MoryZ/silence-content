package com.old.silence.content.console.vo;

/**
 * @author moryzang
 */
public class Promotion {
    private String k;
    private String v;


    public Promotion() {
    }

    public Promotion(String k, String v) {
        this.k = k;
        this.v = v;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }
}
